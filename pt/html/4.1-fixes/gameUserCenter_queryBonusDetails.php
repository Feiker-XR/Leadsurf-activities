								if( !wanfa || $t.hasClass('onhandled') ) return false;

								$t.addClass('onhandled');

								var offset = $t.offset();
								var fix = {left: $t.outerWidth()*.75, top: $t.outerHeight()};
								// 获取玩法的数据，如果有
								var wanfaData = $t.data('wanfaData');
								$wanfan.css({
