								// show loading
								$wanfaReplace.animate({opacity: 0.4});
								$wanfaLoading.fadeIn('fast');

								if( wanfaData ){
									loadWanfa( wanfaData, $t );
								}else{
									$.get(ajaxurl, {wanfa: wanfa}, function(resp){
										resp = $.parseJSON(resp);
										if( resp.status == 'ok' ){
											loadWanfa( resp.html, $t );
										}
									});
								}
								return false;
							});
							
