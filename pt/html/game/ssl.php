				var currentGameMethod = phoenix.Games.getCurrentGame().getCurrentGameMethod();

				if(!dom.is(':hidden')){
					currentGameMethod.getChart(modeName, function(chart){
						dom.html(chart);
					});
				}
			});

			//走势图展开操作
			$('.chart-switch').bind('click', function(){
				var currentGameMethod = phoenix.Games.getCurrentGame().getCurrentGameMethod(),
					modeName = currentGameMethod.getGameMethodName();

				if(!dom.is(':hidden')){
					dom.hide();
				}else{
					currentGameMethod.getChart(modeName, function(chart){
						dom.html(chart);
					});
					dom.show();
				}
			});
		})();
		
