				break;
				case 'houer.zhixuan.fushi':
					gameBallLength = 2;
				break;
				case 'qianer.zhixuan.fushi':
					gameBallLength = 2;
				break;
				case 'yixing.dingweidan.fushi':
					gameBallLength = 1;
				break;
				default:
				return false; 
			}

			if(current.length != gameBallLength) {
				return false;
			} else {
				for (var i = current.length - 1; i >= 0; i--) {
					if(current[i].join('') == '') {
						return false;
					};
				};
